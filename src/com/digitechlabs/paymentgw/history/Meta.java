package com.digitechlabs.paymentgw.history;

public class Meta {

    private int page;
    private int total;
    private int per_page;

    public Meta(int page, int total, int per_page) {
        this.page = page;
        this.total = total;
        this.per_page = per_page;
    }

    public int getPage() {
        return page;
    }

    public void setPage(int page) {
        this.page = page;
    }

    public int getTotal() {
        return total;
    }

    public void setTotal(int total) {
        this.total = total;
    }

    public int getPer_page() {
        return per_page;
    }

    public void setPer_page(int per_page) {
        this.per_page = per_page;
    }

}
